            <div class="text">
              <p>Московский ЦСКА потерпел разгромное поражение от итальянской «Ромы» в матче первого тура группового этапа Лиги чемпионов. Встреча, которая проходила в Риме, завершилась со счетом 5:1 в пользу итальянцев.</p>
              <p>Пропустив в 22-м подряд матче Лиги чемпионов, голкипер ЦСКА Игорь Акинфеев продлил антирекордную серию. В прошлом розыгрыше турнира вратарь провел шесть игр в рамках группового этапа и пропустил 17 мячей. Во время второго тайма болельщики ЦСКА стали зажигать пиротехнику и бросать ее на трибуну итальянских фанатов. Перед окончанием матча диктор стадиона объявил, что фанатам красно-синих предписано оставаться на своих местах некоторое время после окончания игры. Подобные действия обычно проводятся для поиска нарушителей порядка и предотвращения драк между болельщиками игравших команд.</p>
              <p>Московский ЦСКА потерпел разгромное поражение от итальянской «Ромы» в матче первого тура группового этапа Лиги чемпионов. Встреча, которая проходила в Риме, завершилась со счетом 5:1 в пользу итальянцев.</p>
